import { useState } from "react";
import { router, usePathname } from "expo-router";
import { View, TouchableOpacity, Image, TextInput, Alert } from "react-native";

import { icons, styles } from "../constants";

export interface InitialQueryPropType {
  initialQuery?: string;
  placeholder?: string;
}

const SearchInput = ({ initialQuery, placeholder }: InitialQueryPropType) => {
  const pathname = usePathname();
  const [query, setQuery] = useState(initialQuery || "");

  return (
    <View className="flex flex-row items-center space-x-4 w-full h-14 px-4 bg-black-100 rounded-2xl border border-black-200 focus:border-secondary">
      <TextInput
        className="text-base mt-0.5 text-white flex-1 font-pregular"
        value={query}
        placeholder={placeholder || "Search a topic"}
        placeholderTextColor={styles.colors.gray[100]}
        onChangeText={(e) => setQuery(e)}
      />

      <TouchableOpacity
        onPress={() => {
          if (query === "")
            return Alert.alert(
              "Missing Query",
              "Please input something to search results across database"
            );

          // for later
          //   if (pathname.startsWith("/search")) router.setParams({ query });
          //   else router.push(`/search/${query}`);
        }}
      >
        <Image source={icons.search} className="w-5 h-5" resizeMode="contain" />
      </TouchableOpacity>
    </View>
  );
};

export default SearchInput;
